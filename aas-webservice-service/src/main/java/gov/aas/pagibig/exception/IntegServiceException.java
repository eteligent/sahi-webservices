package gov.aas.pagibig.exception;

import gov.aas.pagibig.common.exception.ErrorCode;
import gov.aas.pagibig.common.exception.IISPException;

@SuppressWarnings( "serial" )
public class IntegServiceException
    extends IISPException
{

    public IntegServiceException()
    {
        super();
    }

    public IntegServiceException( String message, ErrorCode errorCode, Throwable cause )
    {
        super( message, errorCode, cause );
    }

    public IntegServiceException( String message, ErrorCode errorCode )
    {
        super( message, errorCode );
    }

    public IntegServiceException( String message, Throwable cause )
    {
        super( message, cause );
    }

    public IntegServiceException( String message )
    {
        super( message );
    }

    public IntegServiceException( Throwable cause )
    {
        super( cause );
    }

}
