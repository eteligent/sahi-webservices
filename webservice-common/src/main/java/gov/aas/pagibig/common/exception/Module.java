package gov.aas.pagibig.common.exception;

public enum Module
{
    SERVICE( "SRV", "Service" ),
    WS_01( "WS-05", "WS-05" ),
    WS_02( "WS-05", "WS-05" ),
    WS_03( "WS-05", "WS-05" ),
    WS_04( "WS-05", "WS-05" ),
    WS_05( "WS-05", "WS-05" ),
    WS_06( "WS-05", "WS-05" ),
    WS_07( "WS-05", "WS-05" ),
    WS_08( "WS-05", "WS-05" );

    private final String code;

    private final String description;

    private Module( String code, String description )
    {
        this.code = code;
        this.description = description;
    }

    public String getCode()
    {
        return code;
    }

    public String getDescription()
    {
        return description;
    }

    @Override
    public String toString()
    {
        return description;
    }
}
